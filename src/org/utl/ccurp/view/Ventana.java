    public void calcularCurp(){
        
        Persona persona = new Persona();
        String apellido = txtApellidoPat.getText();
        persona.setApellido(apellido);
        String apellido2 = txtApellidoMat.getText();
        persona.setApellido2(apellido2);
        String nombre = txtNombre.getText();
        persona.setNombre(nombre);
        String nombre2 = txtNombre2.getText();
        persona.setNombre2(nombre2);
        int anio = Integer.parseInt(txtAnio.getText());
        persona.setAnio(anio);
        int mes = Integer.parseInt(txtMes.getText());
        persona.setMes(mes);
        int dia = Integer.parseInt(txtDia.getText());
        persona.setDia(dia);
        char genero = txtGenero.getText().charAt(0);
        persona.setGenero(genero);
        String estado = txtEstado.getText();
        persona.setEstado(estado);
        
        

        
        CalculadoraCURP calcCurp = new CalculadoraCURP();
        txtaCurp.setText(calcCurp.calcularCurp(persona));
        
    }
